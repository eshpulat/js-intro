// Greeting Message Based on Time
var random = Math.floor(Math.random() * 25);

if (random >= 0 && random <= 12) {
    console.log("Good morning!");
} else if (random >= 12 && random <= 17) {
    console.log("Good afternoon!");
} else {
    console.log("Good evening!");
}

